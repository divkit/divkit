<script lang="ts">
    import { createEventDispatcher, getContext, onDestroy } from 'svelte';
    import type { VideoSource } from '../../utils/video';
    import { APP_CTX, type AppContext } from '../../ctx/appContext';

    export let value: VideoSource;

    const { state, videoSource2Dialog } = getContext<AppContext>(APP_CTX);
    const { readOnly } = state;

    const dispatch = createEventDispatcher();

    let elem: HTMLElement;
    let url = '';

    function updateVal(value: VideoSource): void {
        url = value.url || '';
    }

    $: updateVal(value);

    function onClick(): void {
        videoSource2Dialog().show({
            value,
            target: elem,
            readOnly: $readOnly,
            callback(newValue) {
                value = newValue;
                dispatch('change');
            }
        });
    }

    onDestroy(() => {
        videoSource2Dialog().hide();
    });
</script>

<button
    class="video-sources-item"
    class:video-sources-item_readonly={$readOnly}
    bind:this={elem}
    on:click={onClick}
>
    <div class="video-sources-item__url">
        {url}
    </div>
</button>

